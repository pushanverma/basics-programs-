import java.util.*;


public class countingfor {

    public static void main(String[] args)
    {
        Scanner scn = new Scanner(System.in);
        int n;
        System.out.println("\nEnter no.=");
        n=scn.nextInt();
        for(int i=0;i<n;i++)
        {
            System.out.println(i+"\n");
        }
    }
    
}